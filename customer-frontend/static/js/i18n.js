// Current language (default: English)
let currentLanguage = localStorage.getItem('selectedLanguage') || 'en';

// Get translated product name by product id (fallback to original name)
function getProductName(productId, fallbackName) {
    const lang = translations[currentLanguage];
    if (lang && lang.productNames && lang.productNames[productId])
        return lang.productNames[productId];
    return fallbackName || '';
}

// Get translation string by key (for dynamic content)
function getTranslation(key) {
    const lang = translations[currentLanguage];
    return (lang && lang[key]) ? lang[key] : key;
}

// Function to change language
function changeLanguage(langCode) {
    currentLanguage = langCode;
    localStorage.setItem('selectedLanguage', langCode);
    applyTranslations();

    // Re-render product names and cart when language changes
    if (typeof window.onLanguageChange === 'function') window.onLanguageChange();

    // Show toast notification
    const langNames = {
        en: 'English',
        hi: 'हिन्दी',
        ta: 'தமிழ்',
        te: 'తెలుగు',
        kn: 'ಕನ್ನಡ',
        ml: 'മലയാളം'
    };

    if (typeof showToast === 'function') {
        showToast(`Language changed to ${langNames[langCode]}`, 'info');
    }
}

// Function to apply translations to the page
function applyTranslations() {
    const lang = translations[currentLanguage];
    if (!lang) return;

    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (lang[key]) {
            // For input placeholders
            if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = lang[key];
            } else {
                // For regular text content
                element.textContent = lang[key];
            }
        }
    });

    // Special handling for dynamic content
    updateDynamicContent(lang);
}

// Update dynamic content that may not have data-i18n attributes
function updateDynamicContent(lang) {
    // This function can be extended to handle specific dynamic elements
    // For now, it's a placeholder for future enhancements

    // Example: Update basket count badge if it exists
    const basketBadge = document.getElementById('basket-count-badge');
    if (basketBadge && basketBadge.textContent.includes('Items')) {
        const count = basketBadge.textContent.split(' ')[0];
        basketBadge.textContent = `${count} ${lang.items}`;
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    // Set the language selector to the saved language
    const selector = document.getElementById('language-selector');
    if (selector) {
        selector.value = currentLanguage;
    }

    // Apply translations
    applyTranslations();
});

// Make changeLanguage and getProductName available globally
window.changeLanguage = changeLanguage;
window.getProductName = getProductName;
window.getTranslation = getTranslation;
